def codigo_para_opcao(codigo: str) -> str | None:
    """Converte código de estrutura (ex: 'A1') para a string de opção do selectbox."""
    desc = ESTRUTURA.get(codigo)
    return f"{codigo} — {desc}" if desc else None


def opcao_para_codigo(opcao: str) -> str | None:
    """Extrai o código da string de opção selecionada no selectbox."""
    if not opcao or " — " not in opcao:
        return None
    return opcao.split(" — ")[0].strip()
